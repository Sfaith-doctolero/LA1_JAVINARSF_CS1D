// Comparison or Relational Operators
let a = 10, b = 20;  

console.log("Not Equal:", a != b);  //Loose Inequality: convert to common type, then see if the values are not equal 
console.log("Greater Than:", a > b);  //Check if first value is greater than the second one
console.log("Less Than:", a < b);   //Check if first value is less than the second one
console.log("Strictly Equal:", a === "10");  // Checks if both value and type are the same
console.log("Loosely Equal:", a == "10");    // //Loose Equality: convert to common type, then see if they have equal values
