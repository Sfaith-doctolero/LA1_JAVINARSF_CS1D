// Numbers Activity
// Using numbers and the Basic math operators
//Using addition(+), subtraction(-), Multiplication(*), division(/), Modulus(%), and Exponentiation(**)

const num1 = 10, num2 = 5;  

console.log("Addition:", num1 + num2);  
console.log("Subtraction:", num1 - num2);  
console.log("Multiplication:", num1 * num2);  
console.log("Division:", num1 / num2);  
console.log("Modulus:", num1 % num2);  
console.log("Exponentiation:", num1 ** num2);  

// Using Increment and Decrement Operations
let counter = 5;

console.log("Initial Counter:", counter);

console.log("Post-Increment:", counter++);  // Returns initial value, then add 1
console.log("After Post-Increment:", counter);  

console.log("Pre-Increment:", ++counter);  // Increases first, then returns 

console.log("Post-Decrement:", counter--);  // Returns initial, then decreases 1
console.log("After Post-Decrement:", counter);  

console.log("Pre-Decrement:", --counter);  // Decreases first, then returns 
